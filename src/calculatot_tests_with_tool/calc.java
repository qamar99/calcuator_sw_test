package calculatot_tests_with_tool;


public class calc {
	
		public int add(int a, int b )	
		{
			return a+b;
		}
	
		public int sub(int a, int b )	
		{
			return a-b;
		}
	
		public int mult(int a, int b )	
		{
			return a*b;
		}
	
		public int div(int a, int b )	
		{
			return a/b;
		}
	
		
	
}
